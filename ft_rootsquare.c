#include "libft.h"

int		ft_rootsquare(int n)
{
	int		i;

	i = 1;
	if (n == 0)
		return (0);
	while (i < (n / 2))	
	{
		if ((i * i) >= n)
			return (ft_abs(i));
		i++;
	}
}
