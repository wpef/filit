#include "libft.h"

char	*ft_strcat(char *restrict dest, const char *restrict src)
{
	size_t		i;
	size_t		j;

	j = 0;
	i = ft_strlen(dest);
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
